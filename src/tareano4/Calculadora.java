    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        double segundoNumero = Double.parseDouble(jTextField1.getText().isEmpty() ? String.valueOf(primerNumero) : jTextField1.getText());
        double resultado = 0;
        
        switch (operacion) {
            case "suma" ->{
                resultado = primerNumero + segundoNumero;
                jTextField1.setText(String.valueOf(resultado));
            }
            case "resta" ->{
                resultado = primerNumero - segundoNumero;
                jTextField1.setText(String.valueOf(resultado));
            }
            case "multi" -> {
                resultado = primerNumero * segundoNumero;
                jTextField1.setText(String.valueOf(resultado));
            }
            case "divi" ->{
                resultado = primerNumero/segundoNumero;
                jTextField1.setText(String.valueOf(resultado));
            }
            case "potencia"->{
                resultado = Math.pow(primerNumero, segundoNumero);
                jTextField1.setText(String.valueOf(resultado));
            }
            case "sumatoria"->{
                for (double i = primerNumero; i <= segundoNumero; i++){
                    resultado += i;
                }
                jTextField1.setText(String.valueOf(resultado));
            }
            case "productoria"->{
                for (double i = (primerNumero + 1); i <= segundoNumero; i++){
                    primerNumero *= i;
                }
                
                resultado = primerNumero;
                jTextField1.setText(String.valueOf(resultado));
               
            }
            case "pitagoras"->{
                double primero = Math.pow(primerNumero, 2);
                double segundo = Math.pow(segundoNumero, 2);
                double respuesta = primero + segundo;
                resultado = Math.pow(respuesta, 1.0/2.0);
                jTextField1.setText(String.valueOf(resultado));
            }
            case "permutacion"->{
                double numerador = factorial(primerNumero);
                double r = primerNumero - segundoNumero;
                double denomiador = factorial(r);
                resultado = numerador / denomiador;
                jTextField1.setText(String.valueOf(resultado));
            }
            case "combinacion"->{
                double numerador = factorial(primerNumero);
                double resta = primerNumero - segundoNumero;
                double factorial1 = factorial(resta);
                double factorial2 = factorial(segundoNumero);
                double denominador = factorial1*factorial2;
                resultado = numerador/denominador;
                jTextField1.setText(String.valueOf(resultado));
            }
            
               
        }
    }//GEN-LAST:event_jButton16ActionPerformed

